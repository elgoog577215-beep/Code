-- 学科质量第一批修正：范围映射、关系完整性、分类治理和高引用知识点精修。
-- 稳定 code 不改名；历史失效对象保留但退出启用导航。

CREATE TABLE public.informatics_discipline_scope_mappings (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    knowledge_node_code character varying(160) NOT NULL,
    framework_code character varying(80) NOT NULL,
    scope_code character varying(120) NOT NULL,
    coverage_role character varying(40) NOT NULL,
    source_reference character varying(1000) NOT NULL,
    evidence_note character varying(1200) NOT NULL,
    review_status character varying(40) NOT NULL,
    enabled boolean NOT NULL DEFAULT true,
    created_at timestamp(6) without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamp(6) without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_discipline_scope_knowledge_node
        FOREIGN KEY (knowledge_node_code)
        REFERENCES public.informatics_knowledge_nodes(code)
        ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT uk_discipline_scope_mapping
        UNIQUE (knowledge_node_code, framework_code, scope_code),
    CONSTRAINT ck_discipline_scope_coverage_role
        CHECK (coverage_role IN ('FOUNDATION', 'EXTENSION', 'PRACTICE')),
    CONSTRAINT ck_discipline_scope_review_status
        CHECK (review_status IN ('SOURCE_ANCHORED', 'TEACHER_REVIEWED', 'INFERRED'))
);

CREATE INDEX idx_discipline_scope_framework
    ON public.informatics_discipline_scope_mappings(framework_code, scope_code)
    WHERE enabled = true;

CREATE INDEX idx_discipline_scope_knowledge
    ON public.informatics_discipline_scope_mappings(knowledge_node_code)
    WHERE enabled = true;

