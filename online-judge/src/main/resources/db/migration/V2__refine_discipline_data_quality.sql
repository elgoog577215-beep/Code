-- 启用关系只允许连接启用对象；历史失效关系保留但停用。
WITH active_objects(entity_type, code) AS (
    SELECT 'KNOWLEDGE_NODE', code FROM public.informatics_knowledge_nodes WHERE enabled = true
    UNION ALL SELECT 'SKILL_UNIT', code FROM public.ai_standard_skill_units WHERE enabled = true
    UNION ALL SELECT 'MISTAKE_POINT', code FROM public.ai_standard_mistake_points WHERE enabled = true
    UNION ALL SELECT 'IMPROVEMENT_POINT', code FROM public.ai_standard_improvement_points WHERE enabled = true
)
UPDATE public.ai_standard_library_relations r
SET enabled = false,
    updated_at = CURRENT_TIMESTAMP
WHERE r.enabled = true
  AND (
      NOT EXISTS (
          SELECT 1 FROM active_objects a
          WHERE a.entity_type = r.source_type AND a.code = r.source_code
      )
      OR NOT EXISTS (
          SELECT 1 FROM active_objects a
          WHERE a.entity_type = r.target_type AND a.code = r.target_code
      )
  );

